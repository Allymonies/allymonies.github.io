function getRecipe(item, machine=false) {
	if (machine && recipes.hasOwnProperty(machine)) {
		if (recipes[machine].hasOwnProperty(item)) {
			return recipes[machine][item];
		} else {
			return false;
		}
	} else {
		for (var machine in recipes) {
			if (recipes.hasOwnProperty(machine) && recipes[machine].hasOwnProperty(item)) {
				return recipes[machine][item];
			}
		}
	}
	return false;
}

function addRecipe(machine, item, ingredients) {
	if (!recipes.hasOwnProperty(machine)) {
		return false;
	}
	recipes[machine][item] = {
		"machine":machine,
		"ingredients": ingredients
	}
	indexRecipes();
	return recipes[machine][item];
}

function addMachine(machine) {
	recipes[machine] = {};
	return recipes[machine];
}

function indexRecipes() {
	indexes = {}
	for (var machine in recipes) {
		if (recipes.hasOwnProperty(machine))  {
			for (var item in recipes[machine]) {
				if (recipes[machine].hasOwnProperty(item) && !indexes.hasOwnProperty(item)) {
					if (Array.isArray(recipes[machine][item]["ingredients"][0])) {
						var ingredients = recipes[machine][item]["ingredients"][0];
					} else {
						var ingredients = recipes[machine][item]["ingredients"];
					}
					var highestIndex = 0;
					for (var ingredient in ingredients) {
						if (ingredients.hasOwnProperty(ingredient)) {
							var curIndex = indexItem(ingredient);
							if (curIndex > highestIndex) {
								highestIndex = curIndex;
							}
						}
					}
					indexes[item] = highestIndex + 1
				}
			}
		}
	}
}

function indexItem(item, recursion=0) {
	if (indexes.hasOwnProperty(item)) {
		return indexes[item];
	}
	if (recursion > 100) {
		return 100;
	}
	for (var machine in recipes) {
		if (recipes.hasOwnProperty(machine) && recipes[machine].hasOwnProperty(item))  {
			if (Array.isArray(recipes[machine][item]["ingredients"][0])) {
				var ingredients = recipes[machine][item]["ingredients"][0];
			} else {
				var ingredients = recipes[machine][item]["ingredients"];
			}
			var highestIndex = 0;
			for (var ingredient in ingredients) {
				if (ingredients.hasOwnProperty(ingredient)) {
					var curIndex = indexItem(ingredient, recursion+1);
					if (curIndex > highestIndex) {
						highestIndex = curIndex;
					}
				}
			}
			indexes[item] = highestIndex + 1;
			return highestIndex + 1;
		}
	}
}

function calculateCost(item, costs={}, level=0) {
	for (var machine in recipes) {
		if (recipes.hasOwnProperty(machine) && recipes[machine].hasOwnProperty(item))  {
			var recipe = recipes[machine][item]["ingredients"];
			if (Array.isArray(recipe[0])) {
				recipe = recipe[0];
			}
			for (var ingredient in recipe) {
				if (recipe.hasOwnProperty(ingredient)) {
					calculateCost(ingredient, costs, level+1);
				}
			}
			if (!costs.hasOwnProperty(indexes[item])) {
				costs[indexes[item]] = {};
			}
			if (!costs[indexes[item]].hasOwnProperty(item)) {
				costs[indexes[item]][item] = 1;
			} else {
				costs[indexes[item]][item] += 1;
			}
			return costs;
		}
	}
	if (!costs.hasOwnProperty(0)) {
		costs[0] = {};
	}
	if (!costs[0][item]) {
		costs[0][item] = 1;
	} else {
		costs[0][item] += 1;
	}
	return true;
}

